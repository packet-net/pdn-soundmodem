    [Fact]
    public void RoundTrips_Datac0_Bursts_With_Carrier_Frequency_Offset()
    {
        // +33 Hz — inside the burst correlator's ±50 Hz search range (codec2 fmin/fmax defaults)
        // but off the 5 Hz coarse grid, so the fine 1 Hz refinement must do real work.
        const int n = 5;
        const double foffHz = 33.0;
        var mode = OfdmMode.Datac0;
        var payloads = Enumerable.Range(0, n).Select(p => Payload(14, p)).ToList();
        short[] stream = BuildBurstStream(mode, payloads.Select(p => new[] { p }).ToList(), foffHz);

        var rx = new DatacReceiver(mode, packetsPerBurst: 1);
        IReadOnlyList<DatacRxResult> results = rx.Process(stream);
        int crcOk = results.Count(r => r.CrcOk);
        output.WriteLine($"datac0 5×1 bursts +33 Hz: decoded {results.Count}, {crcOk} CRC-OK");

        crcOk.Should().Be(n, "a +33 Hz offset is inside the ±50 Hz burst search range");
        foreach (DatacRxResult r in results.Where(r => r.CrcOk))
        {
            byte[] got = r.Payload.ToArray();
            payloads.Should().ContainEquivalentOf(got, "any CRC-valid packet must carry a transmitted payload");
        }
    }

