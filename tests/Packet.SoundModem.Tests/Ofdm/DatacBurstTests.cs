namespace Packet.SoundModem.Tests.Ofdm;

/// <summary>
/// Validation of the FreeDV datac <b>burst-mode</b> acquisition path (codec2
/// <c>ofdm_sync_search_burst</c> / <c>ofdm_sync_state_machine_data_burst</c>) — the framing the
/// standard CLI tools (<c>freedv_data_raw_tx/rx</c>) and FreeDATA exchange:
/// <c>[silence][preamble][packets…][postamble][silence]</c> per burst, acquired by correlating
/// against the known preamble/postamble instead of the pilots. The decisive fixtures
/// (<c>samples/freedv/datac0_burst.s16</c> etc.) were generated by codec2 1.2.0's own
/// <c>freedv_data_raw_tx</c> and verified decodable by its <c>freedv_data_raw_rx</c> — see
/// <c>samples/freedv/PROVENANCE.md</c> for the exact commands and hashes.
/// </summary>
public class DatacBurstTests(ITestOutputHelper output)
{
    /// <summary>The deterministic per-packet payload the fixtures carry — the same formula the
    /// fixture inputs were generated with (PROVENANCE.md) and the round-trip tests use.</summary>
    private static byte[] Payload(int nbytes, int packet)
    {
        var p = new byte[nbytes];
        for (int i = 0; i < nbytes; i++)
        {
            p[i] = (byte)(((packet * 31) + (i * 17) + 5) & 0xff);
        }

        return p;
    }

    /// <summary>Asserts every burst's payload was recovered, in order, with a valid CRC.</summary>
    private static void AssertAllPayloadsRecovered(
        IReadOnlyList<byte[]> sent, IReadOnlyList<DatacRxResult> results)
    {
        results.Should().HaveCount(sent.Count);
        for (int i = 0; i < sent.Count; i++)
        {
            results[i].CrcOk.Should().BeTrue($"packet {i} must pass CRC");
            results[i].Payload.SequenceEqual(sent[i]).Should().BeTrue($"packet {i} payload must match");
        }
    }

    // ---------------------------------------------------------------------------------------
    // The decisive interop direction: codec2's own freedv_data_raw_tx audio → our burst RX.
    // ---------------------------------------------------------------------------------------

    [Fact]
    public void Decodes_Codec2_Freedv_Data_Raw_Tx_Datac0_Bursts()
    {
        // 5 bursts of 1 datac0 packet each, exactly as `freedv_data_raw_tx datac0 in.bin out.s16`
        // emits them (preamble + packet + postamble + 2 packets of silence per burst). codec2's
        // own freedv_data_raw_rx decodes 5/5; a faithful port must match.
        short[] samples = ReadS16("datac0_burst.s16");
        var sent = Enumerable.Range(0, 5).Select(p => Payload(14, p)).ToList();

        var rx = new DatacReceiver(OfdmMode.Datac0, packetsPerBurst: 1);
        IReadOnlyList<DatacRxResult> results = rx.Process(samples);
        output.WriteLine(
            $"datac0 burst clean: decoded {results.Count}, {results.Count(r => r.CrcOk)} CRC-OK, " +
            $"pre={rx.Demod.PreambleDetections} post={rx.Demod.PostambleDetections} uwFails={rx.Demod.UwFails}");

        AssertAllPayloadsRecovered(sent, results);
        rx.Demod.PreambleDetections.Should().Be(5, "every burst must be acquired via its preamble");
    }

    [Fact]
    public void Decodes_Codec2_Freedv_Data_Raw_Tx_Datac0_Bursts_Through_Noisy_Offset_Channel()
    {
        // The same 5 bursts through codec2's `ch` channel simulator: +22 Hz carrier offset and
        // AWGN at SNR3k ≈ 4.6 dB (see PROVENANCE.md). codec2's freedv_data_raw_rx decodes 5/5.
        short[] samples = ReadS16("datac0_burst_ch.s16");
        var sent = Enumerable.Range(0, 5).Select(p => Payload(14, p)).ToList();

        var rx = new DatacReceiver(OfdmMode.Datac0, packetsPerBurst: 1);
        IReadOnlyList<DatacRxResult> results = rx.Process(samples);
        int crcOk = results.Count(r => r.CrcOk);
        output.WriteLine(
            $"datac0 burst ch(+22Hz, ~4.6dB): decoded {results.Count}, {crcOk} CRC-OK, " +
            $"pre={rx.Demod.PreambleDetections} post={rx.Demod.PostambleDetections} uwFails={rx.Demod.UwFails}");

        // Statistical tier: assert nearly all packets, and that every CRC-OK payload is correct.
        crcOk.Should().BeGreaterThanOrEqualTo(4, "codec2's own RX decodes 5/5 at this SNR/offset");
        int next = 0;
        foreach (DatacRxResult r in results.Where(r => r.CrcOk))
        {
            while (next < sent.Count && !r.Payload.SequenceEqual(sent[next]))
            {
                next++;
            }

            next.Should().BeLessThan(sent.Count, "a CRC-valid packet must carry a transmitted payload");
            next++;
        }
    }

