    [Fact]
    public void Variable_Length_Bursts_Decode_With_End_Of_Burst_Detection()
    {
        // The FreeDvDatacModem shape: the receiver does not know each burst's packet count,
        // so it sets packetsPerBurst to a ceiling and ends each burst via the UW check on
        // the frames after the last real packet (EndOfBurstUwDrop) with the CRC backstop.
        // Bursts of 2, 1 and 3 packets separated by only Nuwframes+1 frames of silence (the
        // modem's guard budget) — far less than codec2's two packets — must all decode, each
        // burst acquired by its own preamble.
        var mode = OfdmMode.Datac0;
        var tx = new DatacTransmitter(mode);
        var bursts = new List<byte[][]>
        {
            new[] { Payload(14, 0), Payload(14, 1) },
            new[] { Payload(14, 2) },
            new[] { Payload(14, 3), Payload(14, 4), Payload(14, 5) },
        };

        var rx = new DatacReceiver(mode, packetsPerBurst: 32, endOfBurstDetection: true);
        int gap = (rx.Demod.Nuwframes + 1) * mode.SamplesPerFrame;
        var audio = new List<Cf>(new Cf[gap]);
        foreach (byte[][] burst in bursts)
        {
            audio.AddRange(tx.ModulateBurst(burst));
            audio.AddRange(new Cf[gap]);
        }

        audio.AddRange(new Cf[2 * mode.SamplesPerPacket]);   // trailing drain
        IReadOnlyList<DatacRxResult> results = rx.Process(DatacTransmitter.ToPcm16(audio.ToArray()));
        output.WriteLine(
            $"datac0 2+1+3 variable bursts: decoded {results.Count}, {results.Count(r => r.CrcOk)} CRC-OK, " +
            $"pre={rx.Demod.PreambleDetections} eobDrops={rx.Demod.EndOfBurstDrops} uwFails={rx.Demod.UwFails}");

        var sent = Enumerable.Range(0, 6).Select(p => Payload(14, p)).ToList();
        AssertAllPayloadsRecovered(sent, results);
        rx.Demod.PreambleDetections.Should().Be(3, "each burst must be acquired via its own preamble");
        results.Select(r => r.PacketInBurst).Should().Equal(0, 1, 0, 0, 1, 2);
    }

    // ---------------------------------------------------------------------------------------
    // The postamble path: acquiring a packet whose preamble the search never saw.
    // ---------------------------------------------------------------------------------------

    [Fact]
    public void Recovers_Second_Packet_Via_Postamble_When_Receiver_Expects_Single_Packet_Bursts()
    {
        // TX sends one burst of TWO packets; the receiver is configured for one packet per burst.
        // After packet 1 decodes, sync drops and the buffered history is cleared, so packet 2's
        // frames flow past in search state — only the postamble detector can recover it: it spots
        // the known seed-3 frame, rewinds rxbufst one packet into the buffered history
        // (ofdm_sync_search_burst, ofdm.c:1355-1364) and packet 2 is demodulated from samples
        // already received. codec2 behaves identically (freedv_data_raw_rx --framesperburst 1 on
        // a --framesperburst 2 transmission decodes 2/2, the second via `nin: 0`, its postamble
        // signature); our RX decodes codec2's own such transmission 2/2 with pre=1/post=1.
        //
        // Whether the rescue lands is payload-dependent in codec2 too: packet 2's data flowing
        // through search can false-trigger the pre/post correlators first (frame-aligned data
        // shares the pilot row with the known sequences), and the resulting failed trial clears
        // the history. These payloads are a verified-rescuing pair — codec2 1.2.0's
        // freedv_data_raw_rx decodes this exact stream 2/2, and payload sets that fail here
        // (e.g. the plain Payload(14, p) pair) fail identically there, false-trial for
        // false-trial. A fully-lost preamble on a single-packet burst is NOT recoverable in
        // either implementation.
        var mode = OfdmMode.Datac0;
        static byte[] RescuePayload(int packet)
        {
            var p = new byte[14];
            for (int i = 0; i < p.Length; i++)
            {
                p[i] = (byte)((284 + (packet * 31) + (i * 17) + 5) & 0xff);
            }

            return p;
        }

        var payloads = new List<byte[]> { RescuePayload(0), RescuePayload(1) };
        short[] stream = BuildBurstStream(mode, [[.. payloads]]);

        var rx = new DatacReceiver(mode, packetsPerBurst: 1);
        IReadOnlyList<DatacRxResult> results = rx.Process(stream);
        output.WriteLine(
            $"datac0 2-packet burst, RX expects 1: decoded {results.Count}, {results.Count(r => r.CrcOk)} CRC-OK, " +
            $"pre={rx.Demod.PreambleDetections} post={rx.Demod.PostambleDetections} uwFails={rx.Demod.UwFails}");

        AssertAllPayloadsRecovered(payloads, results);
        rx.Demod.PreambleDetections.Should().Be(1, "packet 1 is acquired via the preamble");
        rx.Demod.PostambleDetections.Should().Be(1, "packet 2 can only be acquired via the postamble rewind");
        rx.Demod.UwFails.Should().Be(0, "both acquisitions must confirm their unique word first try");
    }

    [Fact]
    public void Postamble_Does_Not_Double_Decode_A_Burst_Acquired_By_Preamble()
    {
        // After a preamble-acquired burst completes, the state machine clears the buffered
        // history (ofdm.c:2202-2207) precisely so the trailing postamble cannot re-trigger a
        // decode of the same packet. 5 bursts in, exactly 5 packets out.
        short[] samples = ReadS16("datac0_burst.s16");
        var rx = new DatacReceiver(OfdmMode.Datac0, packetsPerBurst: 1);
        IReadOnlyList<DatacRxResult> results = rx.Process(samples);

        results.Count(r => r.CrcOk).Should().Be(5, "each burst must decode exactly once");
    }

