namespace Packet.SoundModem.Fec;

/// <summary>
/// The (7,4) Hamming code used by IL2P's optional trailing CRC (spec draft v0.6
/// § Hamming Encode Table / § Hamming Decode Table). The code is transparent: the
/// 4 data bits appear as the least-significant bits of each 7-bit codeword, the
/// three parity bits above them. Both tables are reproduced verbatim from the spec.
/// </summary>
public static class Hamming74
{
    private static readonly byte[] EncodeTable =
    [
        0x0, 0x71, 0x62, 0x13, 0x54, 0x25, 0x36, 0x47,
        0x38, 0x49, 0x5A, 0x2B, 0x6C, 0x1D, 0x0E, 0x7F,
    ];

    private static readonly byte[] DecodeTable =
    [
        0x0, 0x0, 0x0, 0x3, 0x0, 0x5, 0xE, 0x7, 0x0, 0x9, 0xE, 0xB, 0xE, 0xD, 0xE, 0xE,
        0x0, 0x3, 0x3, 0x3, 0x4, 0xD, 0x6, 0x3, 0x8, 0xD, 0xA, 0x3, 0xD, 0xD, 0xE, 0xD,
        0x0, 0x5, 0x2, 0xB, 0x5, 0x5, 0x6, 0x5, 0x8, 0xB, 0xB, 0xB, 0xC, 0x5, 0xE, 0xB,
        0x8, 0x1, 0x6, 0x3, 0x6, 0x5, 0x6, 0x6, 0x8, 0x8, 0x8, 0xB, 0x8, 0xD, 0x6, 0xF,
        0x0, 0x9, 0x2, 0x7, 0x4, 0x7, 0x7, 0x7, 0x9, 0x9, 0xA, 0x9, 0xC, 0x9, 0xE, 0x7,
        0x4, 0x1, 0xA, 0x3, 0x4, 0x4, 0x4, 0x7, 0xA, 0x9, 0xA, 0xA, 0x4, 0xD, 0xA, 0xF,
        0x2, 0x1, 0x2, 0x2, 0xC, 0x5, 0x2, 0x7, 0xC, 0x9, 0x2, 0xB, 0xC, 0xC, 0xC, 0xF,
        0x1, 0x1, 0x2, 0x1, 0x4, 0x1, 0x6, 0xF, 0x8, 0x1, 0xA, 0xF, 0xC, 0xF, 0xF, 0xF,
    ];

    /// <summary>Encodes the low 4 bits of <paramref name="nibble"/> as a 7-bit codeword.</summary>
    public static byte Encode(int nibble) => EncodeTable[nibble & 0xF];

    /// <summary>
    /// Decodes a 7-bit codeword to the most likely 4-bit value (minimum bit distance;
    /// corrects any single-bit error). The high bit of <paramref name="codeword"/> is ignored,
    /// as IL2P pads each codeword to a byte with a zero MSB.
    /// </summary>
    public static byte Decode(byte codeword) => DecodeTable[codeword & 0x7F];
}
